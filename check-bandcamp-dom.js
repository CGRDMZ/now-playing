customDOM = {"bandcamp":false};
/*
check if DOM generated by Bandcamp or twitter:site links to @bandcamp.
test URLs: ["https://store.terminalsoundsystem.com/", "https://music.bennysmiles.com/"].
*/
if((document.querySelector('meta[name="generator"]') !== null &&
document.querySelector('meta[name="generator"]').content === "Bandcamp") ||
(document.querySelector('meta[property="twitter:site"]') !== null &&
document.querySelector('meta[property="twitter:site"]').content === "@bandcamp")){
  customDOM.bandcamp = true;
} 
customDOM;
